#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *d;

	d = (char *)s;
	while (*d)
		d++;
	while (*d != c)
	{
		if (d == s && *d != c)
			return (NULL);
		d--;
	}
	return (d);
}
