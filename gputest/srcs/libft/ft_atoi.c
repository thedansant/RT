int		ft_atoi(char *s)
{
	int i;
	int sign;
	int res;

	i = 0;
	res = 0;
	while ((s[i] >= 8 && s[i] <= 14) || s[i] == ' ')
		i++;
	sign = (s[i] == '-') ? -1 : 1;
	i = (s[i] == '-' || s[i] == '+') ? i + 1 : i;
	while (s[i] >= '0' && s[i] <= '9')
	{
		res = res * 10 + s[i] - '0';
		i++;
	}
	return (sign * res);
}
