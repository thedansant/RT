double	ft_cos(double cx)
{
	double	r;
	double	s;
	int		i;

	r = cx * cx;
	s = 42.0;
	i = 10;
	while (--i >= 1)
		s = 4.0 * i - 2.0 + (-r) / s;
	s *= s;
	return ((s - r) / (s + r));
}
